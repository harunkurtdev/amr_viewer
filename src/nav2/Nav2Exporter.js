/** Ray-casting point-in-polygon test. pts: [[x,y],...] */
function pointInPolygon(x, y, pts) {
    let inside = false;
    for (let i = 0, j = pts.length - 1; i < pts.length; j = i++) {
        const xi = pts[i][0], yi = pts[i][1], xj = pts[j][0], yj = pts[j][1];
        const intersect = ((yi > y) !== (yj > y)) &&
            (x < (xj - xi) * (y - yi) / ((yj - yi) || 1e-12) + xi);
        if (intersect) inside = !inside;
    }
    return inside;
}

/** Quote a string for YAML. */
function q(s) {
    return `"${String(s)}"`;
}

/** Split wheel joints into left/right groups (by name, else by halves). */
function splitLeftRight(joints) {
    const left = joints.filter(n => /left|_l\b|_l_|lhs/i.test(n));
    const right = joints.filter(n => /right|_r\b|_r_|rhs/i.test(n));
    if (left.length && right.length) return { left, right };
    const mid = Math.ceil(joints.length / 2);
    return { left: joints.slice(0, mid), right: joints.slice(mid) };
}

/** Sanitize a polygon name into a valid ROS topic (lowercase, underscores). */
function topicName(name) {
    return String(name).toLowerCase().replace(/[^a-z0-9]+/g, '_').replace(/^_+|_+$/g, '') || 'polygon';
}

/** Collision Monitor polygon names are conventionally PascalCase identifiers. */
function pascal(name) {
    return String(name)
        .replace(/[^a-zA-Z0-9]+/g, ' ')
        .trim()
        .split(/\s+/)
        .map(w => w.charAt(0).toUpperCase() + w.slice(1))
        .join('') || 'Polygon';
}
