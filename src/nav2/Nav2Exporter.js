    // ==================== costmap filters (keepout / speed zones) ====================

    /** Bounding grid for a set of zones, in the map frame. */
    static _zoneGrid(zones, resolution, margin = 1.0) {
        let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
        for (const z of zones) {
            for (const [x, y] of z.points) {
                minX = Math.min(minX, x); minY = Math.min(minY, y);
                maxX = Math.max(maxX, x); maxY = Math.max(maxY, y);
            }
        }
        if (!isFinite(minX)) { minX = -2; minY = -2; maxX = 2; maxY = 2; }
        minX -= margin; minY -= margin; maxX += margin; maxY += margin;
        const res = resolution > 0 ? resolution : 0.05;
        const width = Math.max(1, Math.ceil((maxX - minX) / res));
        const height = Math.max(1, Math.ceil((maxY - minY) / res));
        return { minX, minY, res, width, height };
    }

    /**
     * Rasterize zones into a PGM (P5) mask.
     * valueFn(zone) -> pixel value (0-255) for cells inside that zone.
     * Cells outside every zone get `outside`.
     */
    static _rasterMask(zones, grid, valueFn, outside) {
        const { minX, minY, res, width, height } = grid;
        const header = `P5\n${width} ${height}\n255\n`;
        const headerBytes = new TextEncoder().encode(header);
        const pixels = new Uint8Array(width * height);
        pixels.fill(outside);

        for (let row = 0; row < height; row++) {
            const wy = minY + (height - 1 - row + 0.5) * res; // image row 0 = top (max y)
            for (let col = 0; col < width; col++) {
                const wx = minX + (col + 0.5) * res;
                for (const z of zones) {
                    if (pointInPolygon(wx, wy, z.points)) {
                        pixels[row * width + col] = valueFn(z);
                        break;
                    }
                }
            }
        }
        const out = new Uint8Array(headerBytes.length + pixels.length);
        out.set(headerBytes, 0);
        out.set(pixels, headerBytes.length);
        return out;
    }

    static _maskYaml(imageName, grid, mode) {
        return [
            `image: ${imageName}`,
            'resolution: ' + fmt(grid.res),
            `origin: [${fmt(grid.minX)}, ${fmt(grid.minY)}, 0.0]`,
            'negate: 0',
            'occupied_thresh: 0.65',
            'free_thresh: 0.25',
            `mode: ${mode}`,
            ''
        ].join('\n');
    }

    /** Keepout mask (occupied=keepout). Returns {pgm, yaml} or null. */
    static keepoutMask(config) {
        const zones = config.zones.filter(z => z.enabled && z.type === 'keepout' && z.points.length >= 3);
        if (!zones.length) return null;
        const grid = this._zoneGrid(zones, config.costmap.resolution);
        const pgm = this._rasterMask(zones, grid, () => 0, 255); // inside=0 (occupied)
        return { pgm, yaml: this._maskYaml('keepout_mask.pgm', grid, 'trinary') };
    }

    /** Speed mask (pixel encodes % speed limit). Returns {pgm, yaml} or null. */
    static speedMask(config) {
        const zones = config.zones.filter(z => z.enabled && z.type === 'speed' && z.points.length >= 3);
        if (!zones.length) return null;
        const grid = this._zoneGrid(zones, config.costmap.resolution);
        // mode "scale": occupancy = (255 - p)/255*100  ->  p = 255 - speed*2.55
        const pgm = this._rasterMask(zones, grid,
            (z) => Math.max(0, Math.min(255, Math.round(255 - (z.speedLimit || 0) * 2.55))), 255);
        return { pgm, yaml: this._maskYaml('speed_mask.pgm', grid, 'scale') };
    }

