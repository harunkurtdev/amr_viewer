    /** Bundle everything into a downloadable .zip. */
    static downloadBundle(config) {
        const files = [
            { name: 'nav2_params.yaml', content: this.fullNav2ParamsYaml(config) },
            { name: 'ros2_controllers.yaml', content: this.ros2ControllersYaml(config) },
            { name: 'bringup.launch.py', content: this.bringupLaunchPy() },
            { name: 'nav2_project.json', content: this.projectJson(config) }
        ];

        // Costmap filter masks + filters config (only if zones exist).
        const filters = this.costmapFiltersYaml(config);
        if (filters) files.push({ name: 'costmap_filters.yaml', content: filters });
        const keepout = this.keepoutMask(config);
        if (keepout) {
            files.push({ name: 'keepout_mask.pgm', content: keepout.pgm });
            files.push({ name: 'keepout_mask.yaml', content: keepout.yaml });
        }
        const speed = this.speedMask(config);
        if (speed) {
            files.push({ name: 'speed_mask.pgm', content: speed.pgm });
            files.push({ name: 'speed_mask.yaml', content: speed.yaml });
        }
        const blob = makeZip(files);
        const url = URL.createObjectURL(blob);
        const a = document.createElement('a');
        a.href = url;
        a.download = 'nav2_config.zip';
        document.body.appendChild(a);
        a.click();
        document.body.removeChild(a);
        URL.revokeObjectURL(url);
    }

    /**
     * Top-down footprint as an SVG (vector), for use as a base_footprint
     * drawing/template. ROS top view: +x up, +y left. 1 m = 100 px.
     */
    static footprintSvg(config) {
        const fp = config.footprint;
        let pts;
        if (fp.mode === 'polygon' && fp.points.length >= 3) {
            pts = fp.points;
        } else {
            const r = fp.radius || 0.3;
            pts = [];
            for (let i = 0; i < 48; i++) {
                const a = (i / 48) * Math.PI * 2;
                pts.push([Math.cos(a) * r, Math.sin(a) * r]);
            }
        }
        const S = 100; // px per metre
        // ROS top view: screen x = -y (left positive), screen y = -x (forward up).
        const mapped = pts.map(([x, y]) => [-y * S, -x * S]);
        let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
        for (const [sx, sy] of mapped) {
            minX = Math.min(minX, sx); minY = Math.min(minY, sy);
            maxX = Math.max(maxX, sx); maxY = Math.max(maxY, sy);
        }
        const pad = 30;
        minX -= pad; minY -= pad; maxX += pad; maxY += pad;
        const w = (maxX - minX).toFixed(1), h = (maxY - minY).toFixed(1);
        const poly = mapped.map(([sx, sy]) => `${(sx - minX).toFixed(1)},${(sy - minY).toFixed(1)}`).join(' ');
        const ox = (-minX).toFixed(1), oy = (-minY).toFixed(1); // base origin

        return [
            `<?xml version="1.0" encoding="UTF-8"?>`,
            `<svg xmlns="http://www.w3.org/2000/svg" width="${w}" height="${h}" viewBox="0 0 ${w} ${h}">`,
            `  <!-- Nav2 Editor footprint (top view). +x up, +y left, 1 m = ${S}px. base_frame: ${config.baseFrame} -->`,
            `  <rect width="${w}" height="${h}" fill="#ffffff"/>`,
            `  <polygon points="${poly}" fill="#30d15833" stroke="#1a9e3f" stroke-width="2"/>`,
            `  <line x1="${ox}" y1="${oy}" x2="${ox}" y2="${(Number(oy) - 0.5 * S).toFixed(1)}" stroke="#d11" stroke-width="2"/>`,
            `  <text x="${(Number(ox) + 4)}" y="${(Number(oy) - 0.5 * S + 4).toFixed(1)}" font-size="12" fill="#d11">x</text>`,
            `  <circle cx="${ox}" cy="${oy}" r="3" fill="#0a84ff"/>`,
            `</svg>`,
            ''
        ].join('\n');
    }

    /** Serialize the whole project as JSON. */
    static projectJson(config) {
        return JSON.stringify(config.toJSON(), null, 2);
    }

